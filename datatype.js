// Task 2: Data Types Check

// Creating 5 different types of variables
let name = "Mohammad Khalid Bin Siraj"; // String
let age = 32;                         // Number
let isStudent = false;                 // Boolean
let salary = null;                     // Null (Object type in JS)
let phone;                             // Undefined

// Checking and displaying data types using typeof
console.log("Data type of name: " + typeof name);
console.log("Data type of age: " + typeof age);
console.log("Data type of isStudent: " + typeof isStudent);
console.log("Data type of salary: " + typeof salary);
console.log("Data type of phone: " + typeof phone);